void Error_list(int i){
    if (i==1){
        printf(" Некорректный ввод. Повторите попытку\n");
    } else if( i==2){
        printf(" Некорректный ввод. Такой команды нет. Введите -h для вывода списка команд\n");
    } else if(i==3){
        printf(" Невозможно создать дерево, дерево уже существует! Введите другую команду\n");
    } else if(i==4){
        printf(" Недопустимое значение узла дерева. Введите -n, чтобы посмотреть допустимые значения дерева\n");
    } else if(i==5){
        printf(" Некорректный путь. Повторите команду\n");
    } else if(i==6){
        printf(" Невозможно удалить несуществующий узел дерева\n");
    } else if(i==7){
        printf(" Ошибка исполнения. Дерева не существуе\n");
    }
    else{
        return;
    }
}

