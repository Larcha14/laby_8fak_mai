void print_enum_reference(){
    printf(" Допустимые значение узлов дерева:\n");
    for (int i=1; i<clubs_count;++i){
        printf("%d = %s, ", i, enum_name[i]);
        if (i%8==0){
            printf("\n ");
        }
    }
    printf("\n");
}

int get_int_enum(char* st){
    for (int i=1; i<clubs_count;++i){
        if (strcmp(st, enum_name[i])==0){
            return i;
        }
    }
    return -1;
    
}